/* 
    请求方式：
        1.GET请求：（查）
            用于请求指定的页面信息，并返回一个实体
            数据会在url地址上边发送（查询字符串 queryString）

        2.POST(增)
            - 向指定的资源提交数据进行处理（登录注册）
            - 数据会在报文体中发送

        3.PUT(改)
            - 更改服务器数据
            - 发送文件
        
        4.DELETE(删)
            - 删除指定的数据

        5.OPTIONS（预检）
            - 提前检查服务器支持的请求类型



*/